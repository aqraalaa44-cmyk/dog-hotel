function getCurrentPage() {
  const path = window.location.pathname;
  const page = path.split('/').pop().replace('.html', '') || '1';
  const pageMap = {
    '1': 'home',
    'hazm': 'hazm',
    'footer': 'footer',
    'mmez': 'mmez',
    'claseky': 'claseky',
    'day': 'day',
    'toeal': 'toeal',
    'toasl': 'toasl',
    
  };
  return pageMap[page] || 'home';
}

function changeLanguage(lang) {
  localStorage.setItem('siteLang', lang);
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  translatePage(lang);
  updateLangButton(lang);
}

function translatePage(lang) {
  const page = getCurrentPage();
  const trans = translations[page];
  if (!trans) return;
  
  const t = trans[lang] || trans.ar;
  
  document.title = t.title || page;
  
  const h1 = document.querySelector('h1');
  if (h1) h1.textContent = t.title || h1.textContent;
  
  if (page === 'home') {
    const heroTitle = document.querySelector('.hero-content h1');
    if (heroTitle) heroTitle.textContent = t.title;
    const heroP = document.querySelector('.hero-content p');
    if (heroP) heroP.textContent = t.welcome;
    const heroA = document.querySelector('.hero-content a');
    if (heroA) heroA.textContent = t.homeLink;
    
    const servicesH2 = document.querySelector('.services h2');
    if (servicesH2) servicesH2.textContent = t.servicesTitle;
    
    const items = document.querySelectorAll('.service-item');
    if (items[0]) { items[0].querySelector('h3').textContent = t.s1Title; items[0].querySelector('p').textContent = t.s1Desc; }
    if (items[1]) { items[1].querySelector('h3').textContent = t.s2Title; items[1].querySelector('p').textContent = t.s2Desc; }
    if (items[2]) { items[2].querySelector('h3').textContent = t.s3Title; items[2].querySelector('p').textContent = t.s3Desc; }
    if (items[3]) { items[3].querySelector('h3').textContent = t.s4Title; items[3].querySelector('p').textContent = t.s4Desc; }
    if (items[4]) { items[4].querySelector('h3').textContent = t.s5Title; items[4].querySelector('p').textContent = t.s5Desc; }
    if (items[5]) { items[5].querySelector('h3').textContent = t.s6Title; items[5].querySelector('p').textContent = t.s6Desc; }
    
    const footerA = document.querySelector('footer a');
    if (footerA) footerA.textContent = t.contact;
  }
  
if (page === 'hazm') {
    const pkgs = document.querySelectorAll('.package-item');
    if (pkgs[0]) { 
      pkgs[0].querySelector('h3').textContent = t.p1Title; 
      pkgs[0].querySelector('p').textContent = t.p1Desc; 
      const pkgs0Link = pkgs[0].querySelector('a');
      if (pkgs0Link) pkgs0Link.textContent = t.p1Btn; 
    }
    if (pkgs[1]) { 
      pkgs[1].querySelector('h3').textContent = t.p2Title; 
      pkgs[1].querySelector('p').textContent = t.p2Desc; 
      const pkgs1Link = pkgs[1].querySelector('a');
      if (pkgs1Link) pkgs1Link.textContent = t.p2Btn; 
    }
    if (pkgs[2]) { 
      pkgs[2].querySelector('h3').textContent = t.p3Title; 
      pkgs[2].querySelector('p').textContent = t.p3Desc; 
      const pkgs2Link = pkgs[2].querySelector('a');
      if (pkgs2Link) pkgs2Link.textContent = t.p3Btn; 
    }
    if (pkgs[3]) { 
      pkgs[3].querySelector('h3').textContent = t.p4Title; 
      pkgs[3].querySelector('p').textContent = t.p4Desc; 
      const pkgs3Link = pkgs[3].querySelector('a');
      if (pkgs3Link) pkgs3Link.textContent = t.p4Btn; 
    }
  }
  
  if (page === 'footer') {
    const links = document.querySelectorAll('.button-link');
    if (links[0]) links[0].textContent = t.gmail;
    if (links[1]) links[1].textContent = t.instagram;
    if (links[2]) links[2].textContent = t.facebook;
    if (links[3]) links[3].textContent = t.phone;
    if (links[4]) links[4].textContent = t.support;
  }
  
  if (page === 'mmez' || page === 'claseky' || page === 'day' || page === 'toeal') {
  const contentH2s = document.querySelectorAll('.content h2');
  if (contentH2s[0]) contentH2s[0].textContent = t.sTitle;
  if (contentH2s[1]) contentH2s[1].textContent = t.fTitle;

    const contentP = document.querySelector('.content > p');
    if (contentP) contentP.textContent = t.sDesc;
    
    const lis = document.querySelectorAll('.content ul li');
    if (lis[0]) lis[0].textContent = t.f1;
    if (lis[1]) lis[1].textContent = t.f2;
    if (lis[2]) lis[2].textContent = t.f3;
    if (lis[3]) lis[3].textContent = t.f4;
    
    const btn = document.querySelector('.button');
    if (btn) btn.textContent = t.btn;
  }
  
  if (page === 'toasl') {
    const formH2 = document.querySelector('.form-container h2');
    if (formH2) formH2.textContent = t.formTitle;
    
    const labels = document.querySelectorAll('label');
    if (labels[0]) labels[0].textContent = t.ownerLabel;
    if (labels[1]) labels[1].textContent = t.petLabel;
    if (labels[2]) labels[2].textContent = t.emailLabel;
    if (labels[3]) labels[3].textContent = t.phoneLabel;
    if (labels[4]) labels[4].textContent = t.packageLabel;
    if (labels[5]) labels[5].textContent = t.notesLabel;
    
    const submitBtn = document.querySelector('button[type="submit"]');
    if (submitBtn) submitBtn.textContent = t.submit;
    
  
    const footerA = document.querySelector('footer a');
    if (footerA && t.contact) footerA.textContent = t.contact;
  }
}

function updateLangButton(lang) {
  const btn = document.getElementById('langBtn');
  if (btn) btn.textContent = lang === 'ar' ? 'EN' : 'عربي';
}

function addLanguageButton() {
  const oldBtn = document.getElementById('langBtn');
  if (oldBtn) oldBtn.remove();
  
  const savedLang = localStorage.getItem('siteLang') || 'ar';
  
  const langBtn = document.createElement('button');
  langBtn.id = 'langBtn';
  langBtn.textContent = savedLang === 'ar' ? 'EN' : 'عربي';
  langBtn.style.cssText = `
    position: fixed;
    top: 10px;
    left: 10px;
    padding: 15px 15px;
    min-width: 60px;
    background: #e94560;
    color: white;
    border: none;
    border-radius: 12px;
    cursor: pointer;
    font-weight: bold;
    font-size: 11px;
    z-index: 10001;
    box-shadow: 0 2px 6px rgba(0,0,0,0.3);
    white-space: nowrap;
  `;
  
  langBtn.onclick = () => {
    const currentLang = localStorage.getItem('siteLang') || 'ar';
    changeLanguage(currentLang === 'ar' ? 'en' : 'ar');
  };
  
  document.body.appendChild(langBtn);
  
  document.documentElement.dir = savedLang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = savedLang;
}

document.addEventListener('DOMContentLoaded', () => {
  addLanguageButton();
  setTimeout(() => {
    const savedLang = localStorage.getItem('siteLang') || 'ar';
    translatePage(savedLang);
  }, 100);
});

